# This is from ResNetV2
# Use BN -> ReLU -> Conv
def identity_blockV2(input_tensor, kernel_size, filters, stage, block, use_bn=True):
    """The identity_block is the block that has no conv layer at shortcut
    # Arguments
        input_tensor: input tensor
        kernel_size: defualt 3, the kernel size of middle conv layer at main path
        filters: list of integers, the nb_filters of 3 conv layer at main path
        stage: integer, current stage label, used for generating layer names
        block: 'a','b'..., current block label, used for generating layer names
    """
    nb_filter1, nb_filter2, nb_filter3 = filters
    conv_name_base = 'res' + str(stage) + block + '_branch'
    bn_name_base = 'bn' + str(stage) + block + '_branch'
    x = input_tensor
    if use_bn:
        x = BatchNormalization(axis=3, name=bn_name_base + '2a')(x)
    x = KL.Activation('relu')(x)

    x = KL.Conv2D(nb_filter1, (1, 1), name=conv_name_base + '2a')(x)
    
    if use_bn:
        x = BatchNormalization(axis=3, name=bn_name_base + '2b')(x)
    x = KL.Activation('relu')(x)

    x = KL.Conv2D(nb_filter2, (kernel_size, kernel_size),  padding='same',
                  name=conv_name_base + '2b')(x)
    
    if use_bn:
        x = BatchNormalization(axis=3, name=bn_name_base + '2c')(x)
    x = KL.Activation('relu')(x)

    x = KL.Conv2D(nb_filter3, (1, 1), name=conv_name_base + '2c')(x)
    

    x = KL.Add()([x, input_tensor])
    return x

